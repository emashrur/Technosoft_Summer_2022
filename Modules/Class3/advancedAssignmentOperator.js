/**
 * Advanced Assignment Operator (+=, -=, *=, /=, %=)
 * 
 * 
 */
let num1 = 5;
// used increment operator to add 5 in num1
// num1 ++;
// num1 ++;
// num1 ++;
// num1 ++;
// num1 ++;
num1 = num1 + 5; // or can be denoted by -> num1+=5 
console.log(`num1 -> ${num1}`);  // 10

num1-=2;
console.log(`num1 -> ${num1}`); // 8

num1*=2;
console.log(`num1 -> ${num1}`); // 16

num1/=3;
console.log(`num1 -> ${num1}`); // 5.33

num1%=5;
console.log(`num1 -> ${num1}`); // 0.33

